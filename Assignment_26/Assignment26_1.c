{
    int iCnt = 0;
    printf("%-10s %-10s %-10s\n","Decimal","Hex","Char");
    
    for( iCnt = 0 ; iCnt <= 255 ; iCnt ++ )
    {
        printf("%-10d %-10X %-10c \n",iCnt,iCnt,iCnt);
    }
}

